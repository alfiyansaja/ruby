### Variabel ###

# variabel adalah sebuah tempat
# untuk menyimpan sebuah data, contoh
var_a = "var_a adalah variabel"
puts var_a
var_a = 123
puts var_a
var_a = 0.453
puts var_a
# penamaan sebuah variabel bersifat bebas
# dari huruf kapital A-Z, a-z, awalan underscore(_)
# jangan gunakan angka dan spesial character
# sebagai nama variabel (0-9, !@#$%^, dll)
Coba = "ini juga variabel"
puts Coba
_coba = 140
puts _coba
